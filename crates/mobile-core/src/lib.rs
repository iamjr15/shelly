#[derive(Debug, thiserror::Error, uniffi::Error)]
#[uniffi(flat_error)]
/// Error type mapped across Swift and Kotlin bindings.
pub enum ShellyError {
    /// Client configuration or caller input is invalid.
    #[error("invalid config: {0}")]
    InvalidConfig(String),
    /// iroh transport or stream IO failed.
    #[error("transport error: {0}")]
    Transport(String),
    /// The daemon returned an unexpected or invalid protocol response.
    #[error("protocol error: {0}")]
    Protocol(String),
    /// The daemon rejected the client identity.
    #[error("unauthorized: {0}")]
    Unauthorized(String),
    /// The client is authenticated but not allowed to perform the operation.
    #[error("forbidden: {0}")]
    Forbidden(String),
    /// The requested session or resource does not exist.
    #[error("not found: {0}")]
    NotFound(String),
    /// Internal daemon or mobile-core error.
    #[error("internal error: {0}")]
    Internal(String),
}

#[uniffi::export(callback_interface)]
/// Callback interface for session-list subscriptions.
pub trait SessionListSink: Send + Sync {
    /// Receives a complete replacement session list.
    fn on_update(&self, sessions: Vec<SessionSummaryFfi>);
}

#[uniffi::export(callback_interface)]
/// Callback interface for attached terminal byte streams.
pub trait ByteStreamSink: Send + Sync {
    /// Receives replay or synthetic snapshot bytes immediately after attach.
    fn on_initial_bytes(&self, bytes: Vec<u8>);
    /// Receives raw PTY output bytes.
    fn on_output(&self, bytes: Vec<u8>);
    /// Receives an inferred agent-state change.
    fn on_agent_state(&self, state: AgentStateFfi);
    /// Receives a subscriber-overflow signal; native UI should reattach/resync.
    ///
    /// The v1 wire field is named `skipped_bytes`, but the value is the number
    /// of daemon broadcast messages skipped by the receiver.
    fn on_lag(&self, skipped_bytes: u64);
    /// Receives the process exit code when the session terminates.
    fn on_session_exited(&self, code: i32);
}

