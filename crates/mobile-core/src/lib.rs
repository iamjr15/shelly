enum StreamDecision {
    Continue,
    StopForResync,
    StopCompleted,
}

#[uniffi::export(async_runtime = "tokio")]
impl AttachedSession {
    /// Sends raw input bytes to the attached PTY.
    pub async fn send_input(self: Arc<Self>, bytes: Vec<u8>) -> Result<(), ShellyError> {
        self.write_to_send_stream(&ClientToServerMsg::Input {
            session_id: self.session_id,
            bytes,
        })
        .await
    }

    /// Updates this mobile client's terminal viewport.
    pub async fn resize(self: Arc<Self>, cols: u16, rows: u16) -> Result<(), ShellyError> {
        self.write_to_send_stream(&ClientToServerMsg::Resize {
            session_id: self.session_id,
            size: ClientSize { cols, rows },
        })
        .await
    }

    /// Streams initial bytes, output, lag, state, and exit events to the callback.
    pub async fn subscribe(
        self: Arc<Self>,
        sink: Box<dyn ByteStreamSink>,
    ) -> Result<(), ShellyError> {
        if let Some(bytes) = self.initial_bytes.lock().await.take() {
            sink.on_initial_bytes(bytes);
        }
        let mut recv = self.take_recv().await?;
        loop {
            match self
                .dispatch_stream_message(read_msg::<ServerToClientMsg>(&mut recv).await?, &*sink)
            {
                Ok(StreamDecision::Continue) => {}
                Ok(StreamDecision::StopForResync | StreamDecision::StopCompleted) => break,
                Err(error) => {
                    self.replace_recv(recv).await;
                    return Err(error);
                }
            }
        }
        self.replace_recv(recv).await;
        Ok(())
    }

    /// Detaches this client without killing the desktop session.
    pub async fn detach(self: Arc<Self>) -> Result<(), ShellyError> {
        let mut guard = self.send.lock().await;
        let send = guard.as_mut().ok_or_else(|| {
            ShellyError::Protocol(
                "cannot detach because the terminal send stream is closed".to_string(),
            )
        })?;
        if let Err(error) = write_msg(send, &ClientToServerMsg::DetachSession).await {
            guard.take();
            return Err(error);
        }
        let _ = send.finish();
        Ok(())
    }

    /// Returns the byte offset after the initial attach payload.
    pub fn initial_seq(&self) -> u64 {
        self.initial_seq
    }

    /// Returns the latest raw PTY byte offset observed by this attachment.
    pub fn last_seen_seq(&self) -> u64 {
        self.last_seen_seq.load(Ordering::Acquire)
    }
}

impl AttachedSession {
    fn dispatch_stream_message(
        &self,
        message: ServerToClientMsg,
        sink: &dyn ByteStreamSink,
    ) -> Result<StreamDecision, ShellyError> {
        match message {
            ServerToClientMsg::Output { seq, bytes, .. } => {
                self.last_seen_seq.store(seq, Ordering::Release);
                sink.on_output(bytes);
                Ok(StreamDecision::Continue)
            }
            ServerToClientMsg::AgentStateChanged { state, .. } => {
                sink.on_agent_state(state.into());
                Ok(StreamDecision::Continue)
            }
            ServerToClientMsg::Lag { skipped_bytes, .. } => {
                sink.on_lag(skipped_bytes);
                Ok(StreamDecision::StopForResync)
            }
            ServerToClientMsg::SessionExited { exit_code, .. } => {
                sink.on_session_exited(exit_code);
                Ok(StreamDecision::StopCompleted)
            }
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            _ => Ok(StreamDecision::Continue),
        }
    }

    async fn write_to_send_stream(&self, message: &ClientToServerMsg) -> Result<(), ShellyError> {
        let mut guard = self.send.lock().await;
        let send = guard.as_mut().ok_or_else(|| {
            ShellyError::Protocol(
                "cannot send terminal command because the daemon stream is closed".to_string(),
            )
        })?;
        if let Err(error) = write_msg(send, message).await {
            guard.take();
            return Err(error);
        }
        Ok(())
    }

    async fn take_recv(&self) -> Result<RecvStream, ShellyError> {
        self.recv.lock().await.take().ok_or_else(|| {
            ShellyError::Protocol(
                "cannot subscribe to terminal output because the receive stream is already in use"
                    .to_string(),
            )
        })
    }

    async fn replace_recv(&self, recv: RecvStream) {
        *self.recv.lock().await = Some(recv);
    }
}

impl DaemonTarget {
    fn from_ticket(ticket: &PairingTicket) -> Self {
        Self {
            node_id: ticket.node_id.clone(),
            relay_url: ticket.relay_url.clone(),
            addrs: ticket.addrs.clone(),
        }
    }

    fn from_config(config: &DaemonConfig) -> Self {
        Self {
            node_id: config.daemon_node_id.clone(),
            relay_url: config.relay_url.clone(),
            addrs: config.addrs.clone(),
        }
    }

    fn endpoint_addr(&self) -> Result<EndpointAddr, ShellyError> {
        let endpoint_id = self
            .node_id
            .parse()
            .map_err(|error: iroh::KeyParsingError| {
                ShellyError::Protocol(format!("stored daemon node id is invalid: {error}"))
            })?;
        let mut addrs = Vec::new();
        if let Some(relay_url) = &self.relay_url {
            let relay_url: RelayUrl =
                relay_url
                    .parse()
                    .map_err(|error: iroh::RelayUrlParseError| {
                        ShellyError::Protocol(format!(
                            "stored daemon relay URL is invalid: {error}"
                        ))
                    })?;
            addrs.push(TransportAddr::Relay(relay_url));
        }
        for addr in &self.addrs {
            let addr: SocketAddr = addr.parse().map_err(|error: std::net::AddrParseError| {
                ShellyError::Protocol(format!("stored daemon address is invalid: {error}"))
            })?;
            addrs.push(TransportAddr::Ip(addr));
        }
        Ok(EndpointAddr::from_parts(endpoint_id, addrs))
    }

    fn parsed_relay(&self) -> Result<Option<RelayUrl>, ShellyError> {
        self.relay_url
            .as_ref()
            .map(|relay_url| {
                relay_url
                    .parse::<RelayUrl>()
                    .map_err(|error: iroh::RelayUrlParseError| {
                        ShellyError::Protocol(format!(
                            "stored daemon relay URL is invalid: {error}"
                        ))
                    })
            })
            .transpose()
    }
}

async fn endpoint_for_secret(
    secret_key: &SecretKey,
    relay_url: Option<&RelayUrl>,
) -> Result<Endpoint, ShellyError> {
    // Minimal preset (crypto only) so the phone contacts no n0 server: no n0 DNS
    // publish/resolve, no n0 relays. When the daemon's relay is known (from the
    // pairing ticket or stored pairing) it becomes the only relay; otherwise the
    // endpoint is direct-only (same-network reconnect over the pinned addresses).
    let mut builder = Endpoint::builder(presets::Minimal).secret_key(secret_key.clone());
    if let Some(relay_url) = relay_url {
        builder = builder.relay_mode(RelayMode::custom([relay_url.clone()]));
    }
    builder.bind().await.map_err(|error| {
        ShellyError::Transport(format!(
            "failed to start Shelly mobile network endpoint: {error}"
        ))
    })
}

async fn open_stream(
    endpoint: &Endpoint,
    target: &DaemonTarget,
) -> Result<(SendStream, RecvStream), ShellyError> {
    // The relay (if any) is configured on the endpoint at build time. The target
    // address carries the daemon's stable relay URL plus any direct addresses as
    // connection candidates, so same-network reconnects still go direct.
    let conn = timeout(
        Duration::from_secs(15),
        endpoint.connect(target.endpoint_addr()?, SHELLY_ALPN),
    )
    .await
    .map_err(|_| ShellyError::Transport("timed out connecting to the paired daemon".to_string()))?
    .map_err(|error| {
        ShellyError::Transport(format!("failed to connect to the paired daemon: {error}"))
    })?;
    conn.open_bi().await.map_err(|error| {
        ShellyError::Transport(format!("failed to open daemon control stream: {error}"))
    })
}

