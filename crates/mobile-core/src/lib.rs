#[uniffi::export(async_runtime = "tokio")]
impl ShellyClient {
    #[uniffi::constructor]
    /// Creates a client, generating a device key when no persisted key is supplied.
    pub fn new(config: ClientConfig) -> Result<Arc<Self>, ShellyError> {
        init_android_logging();
        let secret_key = match &config.device_secret_key {
            Some(bytes) => SecretKey::from_bytes(&bytes.as_slice().try_into().map_err(|_| {
                ShellyError::InvalidConfig(
                    "stored mobile identity key is invalid: expected 32 bytes".to_string(),
                )
            })?),
            None => SecretKey::generate(),
        };

        let daemon = config.paired_daemon.as_ref().map(DaemonTarget::from_config);
        let http = build_http_client()?;

        Ok(Arc::new(Self {
            config,
            secret_key,
            http,
            daemon: Mutex::new(daemon),
            endpoint: Mutex::new(None),
            connected: Mutex::new(false),
        }))
    }

    /// Pairs with a daemon using the compact ticket embedded in the desktop QR.
    ///
    /// The scanned string is a `sh1`-prefixed [`PairingTicket`] carrying both the
    /// daemon's reachability and the short code, so a scan needs no typing.
    pub async fn pair_with_qr(
        self: Arc<Self>,
        qr_payload: String,
    ) -> Result<DaemonInfo, ShellyError> {
        let ticket = PairingTicket::decode(qr_payload.trim()).map_err(|error| {
            ShellyError::Protocol(format!("invalid pairing QR payload: {error}"))
        })?;
        self.pair_with_ticket(ticket).await
    }

    /// Pairs with a daemon using a short typed code resolved through the relay.
    ///
    /// The code is normalized, then exchanged for the daemon's [`PairingTicket`]
    /// via the relay rendezvous endpoint. Requires a configured relay control
    /// URL; the typed-code path is unavailable when no relay is hosted.
    pub async fn pair_with_code(self: Arc<Self>, code: String) -> Result<DaemonInfo, ShellyError> {
        let code = normalize_code(&code);
        let relay_control_url = self
            .config
            .relay_control_url
            .as_deref()
            .ok_or_else(|| {
                ShellyError::InvalidConfig(
                    "typed pairing codes are unavailable because no relay control URL is configured"
                        .to_string(),
                )
            })?
            .trim_end_matches('/');
        let url = format!("{relay_control_url}/v1/pair/resolve/{code}");

        let response = self.http.get(&url).send().await.map_err(|error| {
            let error = error.without_url();
            android_error!("failed to resolve pairing code through relay: {error}");
            ShellyError::Transport(format!(
                "failed to resolve pairing code through the relay: {error}"
            ))
        })?;
        let status = response.status();
        if status == reqwest::StatusCode::NOT_FOUND {
            android_warn!("pairing relay returned HTTP {status} while resolving typed code");
            return Err(ShellyError::NotFound(
                "pairing code not found, expired, or already used".to_string(),
            ));
        }
        if !status.is_success() {
            android_error!("pairing relay returned HTTP {status} while resolving typed code");
            return Err(ShellyError::Protocol(format!(
                "pairing relay returned HTTP {} while resolving the code",
                status
            )));
        }
        let resolved: ResolvePairingResponse = response.json().await.map_err(|error| {
            let error = error.without_url();
            android_error!("pairing relay returned an invalid resolve response: {error}");
            ShellyError::Protocol(format!(
                "pairing relay returned an invalid resolve response: {error}"
            ))
        })?;
        let ticket = PairingTicket::decode(resolved.ticket_blob.trim()).map_err(|error| {
            android_error!("pairing relay returned an invalid pairing ticket: {error}");
            ShellyError::Protocol(format!(
                "pairing relay returned an invalid pairing ticket: {error}"
            ))
        })?;
        self.pair_with_ticket(ticket).await
    }

    /// Connects to a previously paired daemon and verifies authorization with a ping.
    pub async fn connect(self: Arc<Self>) -> Result<(), ShellyError> {
        let target = self.daemon_target().await?;
        let endpoint = self.endpoint(target.parsed_relay()?.as_ref()).await?;
        let (mut send, mut recv) = open_stream(&endpoint, &target).await?;
        write_hello(&mut send, self.config.platform).await?;
        expect_welcome(&mut recv).await?;
        write_msg(&mut send, &ClientToServerMsg::Ping { seq: 1 }).await?;
        match read_msg::<ServerToClientMsg>(&mut recv).await? {
            ServerToClientMsg::Pong { seq: 1 } => {
                *self.connected.lock().await = true;
                Ok(())
            }
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            other => Err(ShellyError::Protocol(format!(
                "unexpected daemon response during connect: {other:?}"
            ))),
        }
    }

    /// Marks the client disconnected locally; active attach streams are closed by their owners.
    pub async fn disconnect(self: Arc<Self>) -> Result<(), ShellyError> {
        *self.connected.lock().await = false;
        Ok(())
    }

    /// Fetches the current session list from the paired daemon.
    pub async fn list_sessions(self: Arc<Self>) -> Result<Vec<SessionSummaryFfi>, ShellyError> {
        let (mut send, mut recv) = self.open_authenticated_stream().await?;
        write_msg(&mut send, &ClientToServerMsg::ListSessions).await?;
        match read_msg::<ServerToClientMsg>(&mut recv).await? {
            ServerToClientMsg::SessionList { sessions } => {
                Ok(sessions.into_iter().map(Into::into).collect())
            }
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            other => Err(ShellyError::Protocol(format!(
                "unexpected daemon response to ListSessions: {other:?}"
            ))),
        }
    }

    /// Creates a new shell session on the paired daemon and returns its summary.
    ///
    /// Mobile create is shell-only: the daemon ignores any command, working
    /// directory, or environment and spawns the user's default shell. The optional
    /// `name` is a label; an empty name asks the daemon to generate a memorable one.
    pub async fn create_session(
        self: Arc<Self>,
        name: Option<String>,
    ) -> Result<SessionSummaryFfi, ShellyError> {
        let (mut send, mut recv) = self.open_authenticated_stream().await?;
        write_msg(
            &mut send,
            &ClientToServerMsg::CreateSession {
                name: name.unwrap_or_default(),
                command: Vec::new(),
                cwd: std::path::PathBuf::from("/"),
                env: std::collections::HashMap::new(),
                size: ClientSize { cols: 80, rows: 24 },
            },
        )
        .await?;
        match read_msg::<ServerToClientMsg>(&mut recv).await? {
            ServerToClientMsg::SessionCreated { summary, .. } => Ok(summary.into()),
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            other => Err(ShellyError::Protocol(format!(
                "unexpected daemon response to CreateSession: {other:?}"
            ))),
        }
    }

    /// Kills a session on the paired daemon.
    ///
    /// Fire-and-forget: the daemon sends no response and treats a missing session
    /// as already killed, so this returns once the request is flushed. The
    /// session-list subscription reflects the removal.
    pub async fn kill_session(self: Arc<Self>, id: String) -> Result<(), ShellyError> {
        let session_id = id
            .parse::<SessionId>()
            .map_err(|error| ShellyError::InvalidConfig(format!("invalid session id: {error}")))?;
        let (mut send, _recv) = self.open_authenticated_stream().await?;
        write_msg(&mut send, &ClientToServerMsg::KillSession { session_id }).await?;
        let _ = send.finish();
        Ok(())
    }

    /// Attaches to a session by id.
    pub async fn attach_session(
        self: Arc<Self>,
        id: String,
    ) -> Result<Arc<AttachedSession>, ShellyError> {
        self.attach_session_from(id, None).await
    }

    /// Attaches to a desktop-created session and asks the daemon to replay from a byte offset.
    pub async fn attach_session_from(
        self: Arc<Self>,
        id: String,
        last_seen_seq: Option<u64>,
    ) -> Result<Arc<AttachedSession>, ShellyError> {
        let session_id = id
            .parse::<SessionId>()
            .map_err(|error| ShellyError::InvalidConfig(format!("invalid session id: {error}")))?;
        let (mut send, mut recv) = self.open_authenticated_stream().await?;
        write_msg(
            &mut send,
            &ClientToServerMsg::AttachSession {
                session_id,
                size: ClientSize { cols: 80, rows: 24 },
                last_seen_seq,
            },
        )
        .await?;
        match read_msg::<ServerToClientMsg>(&mut recv).await? {
            ServerToClientMsg::Attached {
                initial_bytes, seq, ..
            } => Ok(Arc::new(AttachedSession {
                session_id,
                send: Mutex::new(Some(send)),
                recv: Mutex::new(Some(recv)),
                initial_bytes: Mutex::new(Some(initial_bytes)),
                initial_seq: seq,
                last_seen_seq: AtomicU64::new(seq),
            })),
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            other => Err(ShellyError::Protocol(format!(
                "unexpected daemon response to AttachSession: {other:?}"
            ))),
        }
    }

    /// Emits the current session list through a callback.
    pub async fn subscribe_sessions(
        self: Arc<Self>,
        sink: Box<dyn SessionListSink>,
    ) -> Result<(), ShellyError> {
        let (mut send, mut recv) = self.open_authenticated_stream().await?;
        write_msg(&mut send, &ClientToServerMsg::SubscribeSessions).await?;
        loop {
            match read_msg::<ServerToClientMsg>(&mut recv).await? {
                ServerToClientMsg::SessionList { sessions } => {
                    sink.on_update(sessions.into_iter().map(Into::into).collect());
                }
                ServerToClientMsg::Error { code, message } => {
                    return Err(error_from_server(code, message));
                }
                other => {
                    return Err(ShellyError::Protocol(format!(
                        "unexpected daemon response to SubscribeSessions: {other:?}"
                    )));
                }
            }
        }
    }

    /// Registers the native APNs/FCM token with the paired daemon.
    pub async fn register_push_token(
        self: Arc<Self>,
        platform: PushPlatform,
        token: String,
    ) -> Result<(), ShellyError> {
        let (mut send, mut recv) = self.open_authenticated_stream().await?;
        write_msg(
            &mut send,
            &ClientToServerMsg::RegisterPushToken {
                platform: platform.into(),
                token,
            },
        )
        .await?;
        match read_msg::<ServerToClientMsg>(&mut recv).await? {
            ServerToClientMsg::Pong { .. } => Ok(()),
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            other => Err(ShellyError::Protocol(format!(
                "unexpected daemon response to RegisterPushToken: {other:?}"
            ))),
        }
    }

    /// Unregisters the native APNs/FCM token from the paired daemon.
    pub async fn unregister_push_token(
        self: Arc<Self>,
        platform: PushPlatform,
        token: String,
    ) -> Result<(), ShellyError> {
        let (mut send, mut recv) = self.open_authenticated_stream().await?;
        write_msg(
            &mut send,
            &ClientToServerMsg::UnregisterPushToken {
                platform: platform.into(),
                token,
            },
        )
        .await?;
        match read_msg::<ServerToClientMsg>(&mut recv).await? {
            ServerToClientMsg::Pong { .. } => Ok(()),
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            other => Err(ShellyError::Protocol(format!(
                "unexpected daemon response to UnregisterPushToken: {other:?}"
            ))),
        }
    }
}

impl ShellyClient {
    /// Shared dial-and-pair path for both the QR and typed-code flows.
    async fn pair_with_ticket(&self, ticket: PairingTicket) -> Result<DaemonInfo, ShellyError> {
        let target = DaemonTarget::from_ticket(&ticket);
        let endpoint = self.endpoint(target.parsed_relay()?.as_ref()).await?;
        let (mut send, mut recv) = open_stream(&endpoint, &target).await?;

        write_hello(&mut send, self.config.platform).await?;
        expect_welcome(&mut recv).await?;
        write_msg(
            &mut send,
            &ClientToServerMsg::PairWithCode {
                code: ticket.code,
                device_name: self.config.device_name.clone(),
                device_node_id: endpoint.id().to_string(),
            },
        )
        .await?;

        match read_msg::<ServerToClientMsg>(&mut recv).await? {
            ServerToClientMsg::PairingComplete { daemon_node_id } => {
                *self.daemon.lock().await = Some(target.clone());
                *self.connected.lock().await = true;
                Ok(DaemonInfo {
                    daemon_node_id,
                    relay_url: target.relay_url,
                    addrs: target.addrs,
                    device_node_id: endpoint.id().to_string(),
                    device_secret_key: self.secret_key.to_bytes().to_vec(),
                })
            }
            ServerToClientMsg::Error { code, message } => Err(error_from_server(code, message)),
            other => Err(ShellyError::Protocol(format!(
                "unexpected daemon response during pairing: {other:?}"
            ))),
        }
    }

    async fn daemon_target(&self) -> Result<DaemonTarget, ShellyError> {
        self.daemon.lock().await.clone().ok_or_else(|| {
            ShellyError::InvalidConfig(
                "client has no saved daemon pairing; pair this device first".to_string(),
            )
        })
    }

    async fn open_authenticated_stream(&self) -> Result<(SendStream, RecvStream), ShellyError> {
        let target = self.daemon_target().await?;
        let endpoint = self.endpoint(target.parsed_relay()?.as_ref()).await?;
        let (mut send, mut recv) = open_stream(&endpoint, &target).await?;
        write_hello(&mut send, self.config.platform).await?;
        expect_welcome(&mut recv).await?;
        Ok((send, recv))
    }

    async fn endpoint(&self, relay_url: Option<&RelayUrl>) -> Result<Endpoint, ShellyError> {
        let mut endpoint = self.endpoint.lock().await;
        if let Some(endpoint) = endpoint.as_ref() {
            return Ok(endpoint.clone());
        }

        let created = endpoint_for_secret(&self.secret_key, relay_url).await?;
        *endpoint = Some(created.clone());
        Ok(created)
    }
}

