#[cfg(test)]
mod tests {
    use super::*;
    use std::sync::Mutex as StdMutex;

    #[derive(Clone, Debug, Eq, PartialEq)]
    enum SinkEvent {
        Initial(Vec<u8>),
        Output(Vec<u8>),
        AgentState(AgentStateFfi),
        Lag(u64),
        Exited(i32),
    }

    #[derive(Default)]
    struct RecordingSink {
        events: StdMutex<Vec<SinkEvent>>,
    }

    impl RecordingSink {
        fn events(&self) -> Vec<SinkEvent> {
            self.events.lock().expect("events lock poisoned").clone()
        }
    }

    impl ByteStreamSink for RecordingSink {
        fn on_initial_bytes(&self, bytes: Vec<u8>) {
            self.events
                .lock()
                .expect("events lock poisoned")
                .push(SinkEvent::Initial(bytes));
        }

        fn on_output(&self, bytes: Vec<u8>) {
            self.events
                .lock()
                .expect("events lock poisoned")
                .push(SinkEvent::Output(bytes));
        }

        fn on_agent_state(&self, state: AgentStateFfi) {
            self.events
                .lock()
                .expect("events lock poisoned")
                .push(SinkEvent::AgentState(state));
        }

        fn on_lag(&self, skipped_bytes: u64) {
            self.events
                .lock()
                .expect("events lock poisoned")
                .push(SinkEvent::Lag(skipped_bytes));
        }

        fn on_session_exited(&self, code: i32) {
            self.events
                .lock()
                .expect("events lock poisoned")
                .push(SinkEvent::Exited(code));
        }
    }

    #[test]
    fn extracts_json_from_qr_output() {
        let payload =
            "qr\n{\"node_id\":\"n\",\"addrs\":[],\"pair_token\":\"t\",\"expires_at\":1}\n";
        assert_eq!(
            extract_json_payload(payload).unwrap(),
            "{\"node_id\":\"n\",\"addrs\":[],\"pair_token\":\"t\",\"expires_at\":1}"
        );
    }

    #[test]
    fn rejects_invalid_device_secret_key() {
        let result = FieldworkClient::new(ClientConfig {
            device_name: "phone".to_string(),
            platform: MobilePlatform::Ios,
            device_secret_key: Some(vec![1, 2, 3]),
            paired_daemon: None,
        });

        assert!(matches!(result, Err(FieldworkError::InvalidConfig(_))));
    }

    #[test]
    fn stream_output_advances_mobile_reconnect_offset_without_decoding_bytes() {
        let session_id = SessionId::new();
        let attached = test_attached_session(session_id, 7);
        let sink = RecordingSink::default();
        let raw_bytes = vec![0x1b, b'[', b'3', b'1', b'm', 0xff, b'\r', b'\n'];

        let decision = attached
            .dispatch_stream_message(
                ServerToClientMsg::Output {
                    session_id,
                    seq: 42,
                    bytes: raw_bytes.clone(),
                },
                &sink,
            )
            .expect("dispatch output");

        assert_eq!(decision, StreamDecision::Continue);
        assert_eq!(attached.last_seen_seq(), 42);
        assert_eq!(sink.events(), vec![SinkEvent::Output(raw_bytes)]);
    }

    #[test]
    fn yes_head_10000_scale_stream_delivers_all_bytes_without_offset_drift() {
        let session_id = SessionId::new();
        let attached = test_attached_session(session_id, 0);
        let sink = RecordingSink::default();
        let mut expected_stream = Vec::with_capacity(20_000);
        let mut seq = 0;

        for _ in 0..100 {
            let mut chunk = Vec::with_capacity(200);
            for _ in 0..100 {
                chunk.extend_from_slice(b"y\n");
            }
            seq += chunk.len() as u64;
            expected_stream.extend_from_slice(&chunk);

            let decision = attached
                .dispatch_stream_message(
                    ServerToClientMsg::Output {
                        session_id,
                        seq,
                        bytes: chunk,
                    },
                    &sink,
                )
                .expect("dispatch output chunk");

            assert_eq!(decision, StreamDecision::Continue);
            assert_eq!(attached.last_seen_seq(), seq);
        }

        let mut actual_stream = Vec::with_capacity(expected_stream.len());
        for event in sink.events() {
            match event {
                SinkEvent::Output(bytes) => actual_stream.extend(bytes),
                other => panic!("unexpected sink event: {other:?}"),
            }
        }

        assert_eq!(actual_stream, expected_stream);
        assert_eq!(actual_stream.len(), 20_000);
        assert_eq!(attached.last_seen_seq(), 20_000);
    }

    #[test]
    fn lag_event_notifies_native_ui_and_stops_for_resync() {
        let session_id = SessionId::new();
        let attached = test_attached_session(session_id, 9);
        let sink = RecordingSink::default();

        let decision = attached
            .dispatch_stream_message(
                ServerToClientMsg::Lag {
                    session_id,
                    skipped_bytes: 3,
                },
                &sink,
            )
            .expect("dispatch lag");

        assert_eq!(decision, StreamDecision::StopForResync);
        assert_eq!(attached.last_seen_seq(), 9);
        assert_eq!(sink.events(), vec![SinkEvent::Lag(3)]);
    }

    fn test_attached_session(session_id: SessionId, seq: u64) -> AttachedSession {
        AttachedSession {
            session_id,
            send: tokio::sync::Mutex::new(None),
            recv: tokio::sync::Mutex::new(None),
            initial_bytes: tokio::sync::Mutex::new(None),
            initial_seq: seq,
            last_seen_seq: std::sync::atomic::AtomicU64::new(seq),
        }
    }
}

